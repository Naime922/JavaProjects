package SaturdayProjects.week02;

public class Assignment2 {
    public static void main(String[] args) {
        int num = 7;
        num += 5;
        //7 + 5 = 12
        num -= 3;
        // 12 - 3 = 9
        num *= 4;
        // 9 * 4 = 36
        num /= 2;
        // 36 / 2 = 18
        num %= 10;
        // 18 % 10 = 8
        System.out.println(num);

    }
}
