package SaturdayProjects.week02;

public class FlashLight {
    private boolean turnedOn;

    public FlashLight() {
        this.turnedOn = turnedOn;
    }

    public void turnOn(){
        turnedOn = true;
        System.out.println("the light is on");
    }
    public void turnOff(){
        turnedOn = false;
        System.out.println("The light is Off");
    }





}
